public class Pattern {

	public static void main(String[] args) {
		
		for ( int a =1; a<=5; a++) {
			
				System.out.print("*");
			}
		System.out.println("=========="); 
		
		for (int i = 1; i<=4; i++) {
			for (int j=1; j<=5; j++) {
				System.out.print("*");
			}
			
		}
		System.out.println("=================");
		for (int c = 0; c<10; c++) {
			for (int d=0; d<10; d++) {
				System.out.print("* ");
			}
			System.out.println();
		}
		System.out.println("=================");
		
		}
	}


